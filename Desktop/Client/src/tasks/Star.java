package tasks;

public class Star {
	int x;
	int y;
	int radius;
	int annulus;
	int dannulus;
	int boxwidth;
	int threshold;
	
	public Star(int x, int y, int radius, int annulus, int dannulus, int boxwidth, int threshold) {
		this.x = x;
		this.y = y;
		this.radius = radius;
		this.annulus = annulus;
		this.dannulus = dannulus;
		this.boxwidth = boxwidth;
		this.threshold = threshold;
	}

}
